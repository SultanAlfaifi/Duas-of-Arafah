function getSavedReadCount() {
  const direct = Number(localStorage.getItem(STORAGE_KEYS.readCount));
  if (Number.isFinite(direct) && direct >= 0) return clamp(direct, 0, duas.length);

  try {
    const progress = JSON.parse(localStorage.getItem(STORAGE_KEYS.progress) || "{}");
    return clamp(Number(progress.readCount) || 0, 0, duas.length);
  } catch {
    return 0;
  }
}

function saveProgress(readCount) {
  if (readCount === state.lastSavedReadCount) return;
  state.lastSavedReadCount = readCount;
  const currentIndex = clamp(readCount, 0, Math.max(duas.length - 1, 0));
  localStorage.setItem(STORAGE_KEYS.readCount, String(readCount));
  localStorage.setItem(
    STORAGE_KEYS.progress,
    JSON.stringify({
      readCount,
      currentDuaId: duas[currentIndex]?.id || duas.length,
      updatedAt: new Date().toISOString(),
    }),
  );
}

function updateCounters(readCount) {
  state.readCount = readCount;
  readCountEl.textContent = String(readCount);
  remainingCountEl.textContent = String(Math.max(duas.length - readCount, 0));
  totalCountEl.textContent = String(duas.length);
  updateRemainingEstimate(readCount);
  saveProgress(readCount);
}

function splitDuaText(dua) {
  const text = dua.text.trim();
  if (text.length < 260) return [text];

  if (dua.category === "quran" && text.includes("۝")) {
    return text
      .replace(/\s*۝\s*/g, " ۝|")
      .split("|")
      .map((part) => part.trim())
      .filter(Boolean);
  }

  return text
    .replace(/([.؟!])\s+/g, "$1|")
    .split("|")
    .map((part) => part.trim())
    .filter(Boolean);
}

function badgeClass(category) {
  if (category === "quran") return "badge badge--quran";
  if (category === "hadith") return "badge badge--hadith";
  if (category === "salawat") return "badge badge--salawat";
  return "badge";
}

function createCard(dua, offset) {
  const card = document.createElement("article");
  const paragraphs = splitDuaText(dua);
  card.className = `dua-card${dua.text.length > 430 ? " is-long" : ""}`;
  card.dataset.offset = String(offset);
  card.dataset.cardIndex = String(dua.id - 1);
  card.setAttribute("role", "article");
  card.setAttribute("aria-label", `الدعاء ${dua.id} من ${duas.length}`);
  card.innerHTML = `
    <div class="dua-card__meta">
      <span class="${badgeClass(dua.category)}">${escapeHtml(dua.badge)}</span>
      <span class="dua-card__count">الدعاء ${dua.id} من ${duas.length}</span>
    </div>
    <div class="dua-card__body">
      ${paragraphs.map((paragraph) => `<p>${escapeHtml(paragraph)}</p>`).join("")}
    </div>
    <div class="dua-card__source" ${dua.sourceDetail ? `title="${escapeHtml(dua.sourceDetail)}"` : ""}>
      ${dua.source ? `المصدر: ${escapeHtml(dua.source)}` : ""}
    </div>
  `;
  return card;
}

function renderDeck(activeIndex) {
  if (!duas.length) {
    deck.innerHTML = '<div class="empty-state">لم يتم العثور على أدعية للعرض.</div>';
    return;
  }

  if (activeIndex >= duas.length) {
    deck.innerHTML = '<div class="empty-state">أتممت قراءة الأدعية. تقبل الله دعاءك.</div>';
    return;
  }

  deck.replaceChildren();
  const layerCount = Math.min(2, duas.length - activeIndex - 2);
  for (let offset = layerCount + 1; offset >= 2; offset -= 1) {
    const layer = document.createElement("div");
    layer.className = "deck-layer";
    layer.dataset.offset = String(offset);
    layer.setAttribute("aria-hidden", "true");
    deck.appendChild(layer);
  }
  if (duas[activeIndex + 1]) {
    const previewCard = createCard(duas[activeIndex + 1], 1);
    previewCard.classList.add("dua-card--preview");
    previewCard.setAttribute("aria-hidden", "true");
    deck.appendChild(previewCard);
  }
  deck.appendChild(createCard(duas[activeIndex], 0));
}

function updateCardTransforms(fraction) {
  deck.querySelectorAll(".deck-layer").forEach((layer) => {
    const offset = Number(layer.dataset.offset);
    const depth = Math.max(offset - fraction, 0);
    const scale = 1 - depth * 0.045;
    const translateY = depth * 24;
    const opacity = 1 - depth * 0.18;
    layer.style.zIndex = String(10 - offset);
    layer.style.opacity = String(clamp(opacity, 0.25, 0.74));
    layer.style.transform = `translate3d(0, ${translateY}px, 0) scale(${scale})`;
  });

  const preview = deck.querySelector('.dua-card[data-offset="1"]');
  if (preview) {
    const reveal = clamp((fraction - 0.16) / 0.72, 0, 1);
    const scale = 0.955 + reveal * 0.045;
    const translateY = (1 - reveal) * 24;
    preview.style.zIndex = "12";
    preview.style.opacity = String(0.42 + reveal * 0.58);
    preview.style.transform = `translate3d(0, ${translateY}px, 0) scale(${scale})`;
    preview.style.setProperty("--preview-content-opacity", String(reveal));
  }

  const card = deck.querySelector('.dua-card[data-offset="0"]');
  if (!card) return;
  const translateX = state.reducedMotion ? 0 : fraction * 64;
  const rotate = state.reducedMotion ? 0 : fraction * 1.25;
  const opacity = state.reducedMotion ? 1 : 1 - fraction * 0.62;
  const scale = 1 - fraction * 0.014;
  card.style.zIndex = "20";
  card.style.opacity = String(clamp(opacity, 0.22, 1));
  card.style.transform = `translate3d(${translateX}%, 0, 0) scale(${scale}) rotate(${rotate}deg)`;
}

