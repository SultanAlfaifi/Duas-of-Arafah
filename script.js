function clamp(value, min, max) {
  return Math.min(Math.max(value, min), max);
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function formatDuration(totalSeconds) {
  const seconds = Math.max(0, Math.floor(totalSeconds));
  const hours = String(Math.floor(seconds / 3600)).padStart(2, "0");
  const minutes = String(Math.floor((seconds % 3600) / 60)).padStart(2, "0");
  const rest = String(seconds % 60).padStart(2, "0");
  return `${hours}:${minutes}:${rest}`;
}

function currentElapsedSeconds() {
  if (state.paused) return state.elapsedBase;
  return state.elapsedBase + Math.floor((Date.now() - state.elapsedStartedAt) / 1000);
}

function saveElapsed() {
  const elapsed = currentElapsedSeconds();
  localStorage.setItem(STORAGE_KEYS.elapsedTime, String(elapsed));
  return elapsed;
}

function updateRemainingEstimate(readCount = state.readCount) {
  const remaining = Math.max(duas.length - readCount, 0);
  if (!remainingTimeEl) return;

  if (!remaining) {
    remainingTimeEl.textContent = "00:00:00";
    return;
  }

  const elapsed = currentElapsedSeconds();
  const baselineSecondsPerCard = 45;
  const secondsPerCard = readCount > 0 ? clamp(elapsed / readCount, 20, 180) : baselineSecondsPerCard;
  remainingTimeEl.textContent = formatDuration(Math.round(secondsPerCard * remaining));
}

function updateTimer() {
  elapsedTimeEl.textContent = formatDuration(currentElapsedSeconds());
  updateRemainingEstimate();
}

function startTimer() {
  state.elapsedBase = Number(localStorage.getItem(STORAGE_KEYS.elapsedTime)) || 0;
  state.elapsedStartedAt = Date.now();
  updateTimer();

  window.setInterval(() => {
    if (!state.paused) {
      updateTimer();
      saveElapsed();
    }
  }, 1000);

  document.addEventListener("visibilitychange", () => {
    if (document.hidden) {
      state.elapsedBase = saveElapsed();
      state.paused = true;
    } else {
      state.elapsedStartedAt = Date.now();
      state.paused = false;
      updateTimer();
    }
  });

  window.addEventListener("beforeunload", saveElapsed);
}

