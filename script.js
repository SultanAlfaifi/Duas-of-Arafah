function maxDuaIndex() {
  return Math.max(duas.length - 1, 0);
}

function gestureTravelDistance() {
  return clamp(window.innerHeight * 0.38, 240, 360);
}

function wheelTravelDistance() {
  return clamp(window.innerHeight * 0.34, 220, 340);
}

function withInstantScroll(callback) {
  const previousScrollBehavior = root.style.scrollBehavior;
  root.style.scrollBehavior = "auto";
  callback();
  root.style.scrollBehavior = previousScrollBehavior;
}

function scrollToYInstant(top) {
  withInstantScroll(() => window.scrollTo(0, top));
  requestScrollUpdate();
}

function scrollToReaderProgress(progress, behavior = "auto") {
  const nextProgress = clamp(progress, 0, maxDuaIndex());
  const targetY = reader.offsetTop + nextProgress * state.scrollStep;

  if (behavior === "smooth" && !state.reducedMotion) {
    window.scrollTo({ top: targetY, behavior: "smooth" });
  } else {
    scrollToYInstant(targetY);
  }

  return targetY;
}

function easeOutCubic(value) {
  return 1 - Math.pow(1 - value, 3);
}

function readScrollProgress() {
  const sectionTop = reader.offsetTop;
  return clamp((window.scrollY - sectionTop) / state.scrollStep, 0, maxDuaIndex());
}

function updateFromScroll() {
  state.ticking = false;
  const progress = readScrollProgress();
  const nearestProgress = Math.round(progress);
  const isNearSettled = Math.abs(progress - nearestProgress) < 0.06;
  const displayProgress = isNearSettled ? nearestProgress : progress;
  const activeIndex = clamp(Math.floor(displayProgress + 0.0001), 0, maxDuaIndex());
  const fraction = clamp(displayProgress - activeIndex, 0, 1);
  const readCount = clamp(
    isNearSettled ? nearestProgress : activeIndex + (fraction >= CARD_SNAP_THRESHOLD ? 1 : 0),
    0,
    duas.length,
  );

  if (activeIndex !== state.activeIndex || !deck.children.length) {
    state.activeIndex = activeIndex;
    renderDeck(activeIndex);
  }

  if (isNearSettled) {
    state.settledIndex = clamp(nearestProgress, 0, maxDuaIndex());
  }

  updateCardTransforms(fraction);
  updateCounters(readCount);
  updateFloatingState();
}

function requestScrollUpdate() {
  if (!state.ticking) {
    state.ticking = true;
    window.requestAnimationFrame(updateFromScroll);
  }
}

function scheduleReaderSnap() {
  window.clearTimeout(state.snapTimer);
  if (state.navigating || state.gestureActive || state.openModal || !isReaderInteractionZone()) return;
  if (!canStartCardNavigation()) {
    scrollToReaderProgress(state.settledIndex, "auto");
    return;
  }
  state.snapTimer = window.setTimeout(() => {
    if (state.navigating || state.gestureActive || state.openModal || !isReaderInteractionZone()) return;
    if (!canStartCardNavigation()) {
      scrollToReaderProgress(state.settledIndex, "auto");
      return;
    }
    const progress = readScrollProgress();
    const lower = Math.floor(progress);
    const fraction = progress - lower;
    let target = clamp(state.settledIndex, 0, maxDuaIndex());
    if (fraction >= CARD_SNAP_THRESHOLD) {
      target = lower + 1;
    } else if (fraction <= 1 - CARD_SNAP_THRESHOLD) {
      target = lower;
    }
    target = clamp(target, 0, maxDuaIndex());
    if (Math.abs(progress - target) > 0.035) {
      goToDuaIndex(target);
    }
  }, 150);
}

function restoreSavedPosition() {
  const savedReadCount = getSavedReadCount();
  state.settledIndex = clamp(savedReadCount, 0, maxDuaIndex());
  updateCounters(savedReadCount);
  if (!savedReadCount) {
    updateFromScroll();
    return;
  }

  window.requestAnimationFrame(() => {
    scrollToReaderProgress(savedReadCount, "auto");
    window.requestAnimationFrame(() => {
      state.activeIndex = -1;
      updateFromScroll();
    });
  });
}

function clearGestureTimer() {
  window.clearTimeout(state.gestureTimer);
  state.gestureTimer = 0;
}

function resetGestureState() {
  clearGestureTimer();
  state.gestureActive = false;
  state.gestureDirection = 0;
  state.gestureCommitted = false;
  state.wheelTotalDelta = 0;
}

function isAtReaderBoundary(direction) {
  const progress = readScrollProgress();
  if (direction > 0) return progress >= maxDuaIndex() - 0.01;
  if (direction < 0) return progress <= 0.01;
  return false;
}

function lockCardNavigation(duration = CARD_STEP_COOLDOWN) {
  const until = performance.now() + duration;
  state.nextCardAllowedAt = Math.max(state.nextCardAllowedAt, until);
  state.wheelLockedUntil = Math.max(state.wheelLockedUntil, until);
}

function canStartCardNavigation() {
  return performance.now() >= state.nextCardAllowedAt;
}

function isReaderGestureTarget(target) {
  return Boolean(target?.closest?.(".reader"));
}

function resetPointerGesture() {
  state.pointerId = null;
  state.pointerLocked = false;
  state.pointerStartedInReader = false;
  state.pointerIsVertical = false;
}

function beginGesture(direction) {
  if (!direction || state.navigating || isAtReaderBoundary(direction)) return false;
  if (!canStartCardNavigation()) return false;
  const baseIndex = clamp(state.settledIndex, 0, maxDuaIndex());
  if ((direction > 0 && baseIndex >= maxDuaIndex()) || (direction < 0 && baseIndex <= 0)) return false;

  clearGestureTimer();
  state.gestureActive = true;
  state.gestureCommitted = false;
  state.gestureDirection = direction;
  state.gestureBaseIndex = baseIndex;
  state.gestureStartedAt = performance.now();
  scrollToReaderProgress(baseIndex, "auto");
  return true;
}

function commitGesture() {
  if (!state.gestureActive || state.gestureCommitted) return;
  const targetIndex = state.gestureBaseIndex + state.gestureDirection;
  state.gestureCommitted = true;
  state.gestureActive = false;
  lockCardNavigation();
  clearGestureTimer();
  goToDuaIndex(targetIndex);
}

function cancelGesture() {
  if (!state.gestureActive || state.gestureCommitted) return;
  const targetIndex = state.gestureBaseIndex;
  resetGestureState();
  goToDuaIndex(targetIndex);
}

function applyGestureDelta(deltaY, travelDistance) {
  const direction = deltaY > 0 ? 1 : deltaY < 0 ? -1 : 0;
  if (!direction) return false;

  if (!state.gestureActive || state.gestureDirection !== direction) {
    if (!beginGesture(direction)) return false;
  }

  const fraction = clamp(Math.abs(deltaY) / travelDistance, 0, 1);
  scrollToReaderProgress(state.gestureBaseIndex + direction * fraction, "auto");

  if (fraction >= CARD_SNAP_THRESHOLD) {
    commitGesture();
  }

  return true;
}

function scheduleGestureCancel() {
  clearGestureTimer();
  if (!state.gestureActive || state.gestureCommitted) return;
  state.gestureTimer = window.setTimeout(cancelGesture, SNAP_CANCEL_DELAY);
}

function goToDuaIndex(targetIndex) {
  window.cancelAnimationFrame(state.navigationFrame);
  clearGestureTimer();
  const nextIndex = clamp(targetIndex, 0, maxDuaIndex());
  const targetY = reader.offsetTop + nextIndex * state.scrollStep;
  const startY = window.scrollY;
  const distance = targetY - startY;
  const duration = clamp(Math.abs(distance) * 0.7, 210, 340);
  state.navigating = true;
  state.activeIndex = -1;
  if (nextIndex !== state.settledIndex) {
    lockCardNavigation();
  }

  const finishNavigation = () => {
    scrollToYInstant(targetY);
    state.navigating = false;
    state.activeIndex = -1;
    state.settledIndex = nextIndex;
    resetGestureState();
    updateFromScroll();
  };

  if (state.reducedMotion || Math.abs(distance) <= 1.25) {
    finishNavigation();
    return;
  }

  const startedAt = performance.now();
  const animateNavigation = (now) => {
    const progress = clamp((now - startedAt) / duration, 0, 1);
    const nextY = startY + distance * easeOutCubic(progress);
    scrollToYInstant(nextY);

    if (progress < 1 && now - startedAt <= NAVIGATION_TIMEOUT) {
      state.navigationFrame = window.requestAnimationFrame(animateNavigation);
      return;
    }

    finishNavigation();
  };

  state.navigationFrame = window.requestAnimationFrame(animateNavigation);
}

function stepDua(direction) {
  const now = Date.now();
  if (state.navigating || state.gestureActive || !canStartCardNavigation() || now - state.lastStepAt < 380) return;
  state.lastStepAt = now;
  goToDuaIndex(state.settledIndex + direction);
}

function isReaderInteractionZone() {
  const rect = reader.getBoundingClientRect();
  return rect.top < window.innerHeight * 0.86 && rect.bottom > window.innerHeight * 0.18;
}

function setupCardStepNavigation() {
  window.addEventListener(
    "wheel",
    (event) => {
      if (state.openModal) return;
      if (!isReaderInteractionZone()) return;
      const direction = event.deltaY > 0 ? 1 : event.deltaY < 0 ? -1 : 0;
      if (!direction) return;
      if (!state.gestureActive && isAtReaderBoundary(direction)) return;

      event.preventDefault();
      const now = performance.now();
      if (state.navigating || now < state.wheelLockedUntil || !canStartCardNavigation()) {
        return;
      }

      if (!state.gestureActive || state.gestureDirection !== direction) {
        state.wheelTotalDelta = 0;
      }

      state.wheelTotalDelta += event.deltaY;
      applyGestureDelta(state.wheelTotalDelta, wheelTravelDistance());
      scheduleGestureCancel();
    },
    { passive: false },
  );

  window.addEventListener(
    "pointerdown",
    (event) => {
      if (event.pointerType !== "touch") return;
      state.pointerStartedInReader = isReaderGestureTarget(event.target);
      if (!state.pointerStartedInReader || state.openModal || !isReaderInteractionZone()) return;

      event.preventDefault();
      if (state.navigating) return;
      state.pointerId = event.pointerId;
      state.pointerStartY = event.clientY;
      state.pointerStartX = event.clientX;
      state.pointerLocked = false;
      state.pointerIsVertical = false;
      state.touchStartedInReader = false;
      state.touchLocked = false;
      state.touchIsVertical = false;
      event.target?.setPointerCapture?.(event.pointerId);

      if (!canStartCardNavigation()) {
        state.pointerLocked = true;
        scrollToReaderProgress(state.settledIndex, "auto");
      }
    },
    { passive: false, capture: true },
  );

  window.addEventListener(
    "pointermove",
    (event) => {
      if (event.pointerType !== "touch") return;
      if (!state.pointerStartedInReader || state.pointerId !== event.pointerId) return;
      if (state.openModal || !isReaderInteractionZone()) return;

      event.preventDefault();
      if (state.navigating) return;

      if (!canStartCardNavigation()) {
        scrollToReaderProgress(state.settledIndex, "auto");
        return;
      }

      const deltaY = state.pointerStartY - event.clientY;
      const deltaX = state.pointerStartX - event.clientX;

      if (!state.pointerIsVertical) {
        if (Math.abs(deltaY) < 10) return;
        if (Math.abs(deltaY) < Math.abs(deltaX) * 1.25) return;
        state.pointerIsVertical = true;
      }

      const direction = deltaY > 0 ? 1 : deltaY < 0 ? -1 : 0;
      if (!direction) return;
      if (!state.gestureActive && isAtReaderBoundary(direction)) return;
      if (state.navigating || state.pointerLocked || !canStartCardNavigation()) return;

      applyGestureDelta(deltaY, gestureTravelDistance());
      if (state.gestureCommitted) {
        state.pointerLocked = true;
      }
    },
    { passive: false, capture: true },
  );

  const finishPointerGesture = (event) => {
    if (event.pointerType !== "touch") return;
    if (state.pointerId !== null && state.pointerId !== event.pointerId) return;
    if (state.gestureActive && !state.gestureCommitted) {
      cancelGesture();
    }
    resetPointerGesture();
  };

  window.addEventListener("pointerup", finishPointerGesture, { passive: true, capture: true });
  window.addEventListener("pointercancel", finishPointerGesture, { passive: true, capture: true });

  window.addEventListener(
    "touchstart",
    (event) => {
      if (state.pointerStartedInReader) {
        event.preventDefault();
        return;
      }
      const touch = event.touches[0];
      state.touchStartedInReader = isReaderGestureTarget(event.target);
      state.touchStartY = touch.clientY;
      state.touchStartX = touch.clientX;
      state.touchLocked = false;
      state.touchIsVertical = false;

      if (!state.openModal && state.touchStartedInReader && isReaderInteractionZone()) {
        event.preventDefault();
        if (state.navigating) {
          state.touchLocked = true;
          return;
        }
        if (!canStartCardNavigation()) {
          state.touchLocked = true;
          scrollToReaderProgress(state.settledIndex, "auto");
        }
      }
    },
    { passive: false, capture: true },
  );

  window.addEventListener(
    "touchmove",
    (event) => {
      if (state.openModal) return;
      if (state.pointerStartedInReader) {
        event.preventDefault();
        return;
      }
      if (!state.touchStartedInReader) return;
      if (!isReaderInteractionZone()) return;
      event.preventDefault();
      if (state.navigating) return;
      const touch = event.touches[0];
      const deltaY = state.touchStartY - touch.clientY;
      const deltaX = state.touchStartX - touch.clientX;

      if (!canStartCardNavigation()) {
        scrollToReaderProgress(state.settledIndex, "auto");
        return;
      }

      if (!state.touchIsVertical) {
        if (Math.abs(deltaY) < 10) return;
        if (Math.abs(deltaY) < Math.abs(deltaX) * 1.25) return;
        state.touchIsVertical = true;
      }

      const direction = deltaY > 0 ? 1 : deltaY < 0 ? -1 : 0;
      if (!direction) return;
      if (!state.gestureActive && isAtReaderBoundary(direction)) return;

      if (state.navigating || state.touchLocked || !canStartCardNavigation()) return;

      applyGestureDelta(deltaY, gestureTravelDistance());
      if (state.gestureCommitted) {
        state.touchLocked = true;
      }
    },
    { passive: false, capture: true },
  );

  window.addEventListener("touchend", () => {
    if (state.gestureActive && !state.gestureCommitted) {
      cancelGesture();
    }
    state.touchLocked = false;
    state.touchIsVertical = false;
    state.touchStartedInReader = false;
  });

  window.addEventListener("touchcancel", () => {
    if (state.gestureActive && !state.gestureCommitted) {
      cancelGesture();
    }
    state.touchLocked = false;
    state.touchIsVertical = false;
    state.touchStartedInReader = false;
  });
}

function resetAllProgress() {
  Object.values(STORAGE_KEYS).forEach((key) => localStorage.removeItem(key));
  state.elapsedBase = 0;
  state.elapsedStartedAt = Date.now();
  state.lastSavedReadCount = -1;
  state.settledIndex = 0;
  state.nextCardAllowedAt = 0;
  state.wheelLockedUntil = 0;
  state.touchStartedInReader = false;
  resetPointerGesture();
  updateTimer();
  applyTheme("light");
  window.scrollTo({ top: reader.offsetTop, behavior: state.reducedMotion ? "auto" : "smooth" });
  updateCounters(0);
  requestScrollUpdate();
}

function lockPageScroll() {
  if (document.body.classList.contains("modal-open")) return;
  state.scrollLockY = window.scrollY;
  document.body.classList.add("modal-open");
  document.body.style.position = "fixed";
  document.body.style.top = `-${state.scrollLockY}px`;
  document.body.style.left = "0";
  document.body.style.right = "0";
  document.body.style.width = "100%";
}

function unlockPageScroll() {
  const y = state.scrollLockY || 0;
  const previousScrollBehavior = root.style.scrollBehavior;
  root.style.scrollBehavior = "auto";
  document.body.classList.remove("modal-open");
  document.body.style.position = "";
  document.body.style.top = "";
  document.body.style.left = "";
  document.body.style.right = "";
  document.body.style.width = "";
  window.scrollTo(0, y);
  window.requestAnimationFrame(() => window.scrollTo(0, y));
  window.setTimeout(() => {
    window.scrollTo(0, y);
    root.style.scrollBehavior = previousScrollBehavior;
  }, 0);
  return y;
}

function updateFloatingState() {
  if (!scrollTopButton) return;
  const show = window.scrollY > reader.offsetTop - 120;
  scrollTopButton.classList.toggle("is-visible", show);
}

function setupFloatingControls() {
  scrollTopButton?.addEventListener("click", () => {
    window.scrollTo({ top: 0, behavior: state.reducedMotion ? "auto" : "smooth" });
  });
  updateFloatingState();
}

function openModal(modal) {
  window.clearTimeout(state.modalCloseTimer);
  state.lastFocus = document.activeElement;
  state.openModal = modal;
  lockPageScroll();
  modal.classList.remove("is-closing");
  modalBackdrop.classList.remove("is-closing");
  modal.hidden = false;
  modalBackdrop.hidden = false;
  modal.querySelector("[data-modal-close]")?.focus();
}

function closeModal() {
  if (!state.openModal) return;
  const closingModal = state.openModal;
  closingModal.classList.add("is-closing");
  modalBackdrop.classList.add("is-closing");
  state.modalCloseTimer = window.setTimeout(() => {
    closingModal.hidden = true;
    closingModal.classList.remove("is-closing");
    modalBackdrop.hidden = true;
    modalBackdrop.classList.remove("is-closing");
    const restoreY = unlockPageScroll();
    try {
      state.lastFocus?.focus?.({ preventScroll: true });
    } catch {
      state.lastFocus?.focus?.();
    }
    window.requestAnimationFrame(() => window.scrollTo(0, restoreY));
    if (state.openModal === closingModal) state.openModal = null;
  }, state.reducedMotion ? 0 : 190);
}

function trapModalFocus(event) {
  if (!state.openModal || event.key !== "Tab") return;
  const focusable = state.openModal.querySelectorAll(
    'button, [href], input, select, textarea, [tabindex]:not([tabindex="-1"])',
  );
  if (!focusable.length) return;

  const first = focusable[0];
  const last = focusable[focusable.length - 1];
  if (event.shiftKey && document.activeElement === first) {
    event.preventDefault();
    last.focus();
  } else if (!event.shiftKey && document.activeElement === last) {
    event.preventDefault();
    first.focus();
  }
}

function shouldBlockModalScroll(panel, deltaY) {
  if (!panel) return true;
  if (panel.scrollHeight <= panel.clientHeight + 1) return true;
  if (deltaY < 0 && panel.scrollTop <= 0) return true;
  if (deltaY > 0 && panel.scrollTop + panel.clientHeight >= panel.scrollHeight - 1) return true;
  return false;
}

function guardModalWheel(event) {
  if (!state.openModal) return;
  const panel = event.target.closest?.(".modal__panel");
  if (shouldBlockModalScroll(panel, event.deltaY)) {
    event.preventDefault();
  }
  event.stopPropagation();
}

function guardModalTouchStart(event) {
  if (!state.openModal) return;
  state.lastModalTouchY = event.touches[0]?.clientY || 0;
}

function guardModalTouchMove(event) {
  if (!state.openModal) return;
  const panel = event.target.closest?.(".modal__panel");
  const currentY = event.touches[0]?.clientY || state.lastModalTouchY;
  const deltaY = state.lastModalTouchY - currentY;
  if (shouldBlockModalScroll(panel, deltaY)) {
    event.preventDefault();
  }
  state.lastModalTouchY = currentY;
  event.stopPropagation();
}

function setupModals() {
  document.querySelectorAll("[data-modal-target]").forEach((button) => {
    button.addEventListener("click", () => {
      const modal = document.getElementById(button.dataset.modalTarget);
      if (modal) openModal(modal);
    });
  });

  document.querySelectorAll("[data-modal-close]").forEach((button) => {
    button.addEventListener("click", closeModal);
  });

  document.querySelectorAll(".modal").forEach((modal) => {
    modal.addEventListener("click", (event) => {
      if (event.target === modal) closeModal();
    });
  });

  modalBackdrop.addEventListener("click", closeModal);
  confirmReset?.addEventListener("click", () => {
    closeModal();
    window.setTimeout(resetAllProgress, state.reducedMotion ? 0 : 210);
  });
  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") closeModal();
    trapModalFocus(event);
  });
  document.addEventListener("wheel", guardModalWheel, { passive: false, capture: true });
  document.addEventListener("touchstart", guardModalTouchStart, { passive: true, capture: true });
  document.addEventListener("touchmove", guardModalTouchMove, { passive: false, capture: true });
}

function setupKeyboardScroll() {
  document.addEventListener("keydown", (event) => {
    if (state.openModal || event.defaultPrevented) return;
    if (["INPUT", "TEXTAREA", "SELECT"].includes(document.activeElement?.tagName)) return;

    const keyMap = {
      ArrowDown: 1,
      PageDown: 1,
      ArrowUp: -1,
      PageUp: -1,
    };

    if (!Object.hasOwn(keyMap, event.key)) return;
    event.preventDefault();
    stepDua(keyMap[event.key]);
  });
}

function init() {
  history.scrollRestoration = "manual";
  initTheme();
  setupLayout();
  startTimer();
  setupModals();
  setupKeyboardScroll();
  setupFloatingControls();
  setupCardStepNavigation();
  restoreSavedPosition();

  window.addEventListener(
    "scroll",
    () => {
      requestScrollUpdate();
      scheduleReaderSnap();
    },
    { passive: true },
  );
  window.addEventListener("resize", () => {
    setupLayout();
    requestScrollUpdate();
  });
  resetProgress.addEventListener("click", () => {
    const modal = document.getElementById("resetConfirmModal");
    if (modal) openModal(modal);
  });
}

document.addEventListener("DOMContentLoaded", init);
